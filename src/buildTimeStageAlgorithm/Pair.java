package buildTimeStageAlgorithm;

public class Pair {
	private int index1;
	private int index2;
	
	Pair(int i1,int i2){
		index1= i1;
		index2 = i2;
	}

	public int getIndex1() {
		return index1;
	}

	public void setIndex1(int index1) {
		this.index1 = index1;
	}

	public int getIndex2() {
		return index2;
	}

	public void setIndex2(int index2) {
		this.index2 = index2;
	}
	
}
